package chap06;

public class Printer {

	void println(int text) 
	{
		System.out.println(text);
		return;
	}
	
	void println(boolean text) 
	{
		System.out.println(text);
		return;
	}
	
	void println(double text) 
	{
		System.out.println(text);
		return;
	}
	void println(String text) 
	{
		System.out.println(text);
		return;
	}
}
